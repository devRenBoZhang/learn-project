package com.zrb.learn.java.jvm.base;

public class IntegerCal {
    public Integer calInteger(Integer arg) {
        int x = 10;
        int y = 20;
        int z = 30;
        int a = 40;
        int b = 50;
        int c = 60;
        int r = arg + x + y + z + a + b + c;
        return r;
    }
}
